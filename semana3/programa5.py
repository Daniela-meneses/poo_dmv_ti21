numero1 = input("Número1 :") # variable tipo int
numero2 = input("Número2 :") # variable tipo int
suma = int(numero1) + int(numero2) #realiza una operacion de suma de dos numeros
print(suma) #imprime el resultado de la operacion a realizar
resta = int(numero1) - int(numero2) #realiza una operacion de resta de dos numeros
print(resta) #imprime el resultado de la operacion a realizar
multiplicacion = int(numero1) * int(numero2) / 2 #realiza una operacion de multiplicar de dos numeros
print(multiplicacion) #imprime el resultado de la operacion a realizar
division = int(numero1) / int(numero2) #realiza una operacion división de dos numeros
print(division) #imprime el resultado de la operacion a realizar
potencia = int(numero1) ** int(numero2) #realiza una operacion de una potencia 
print(potencia) #imprime el resultado de la operacion a realizar
